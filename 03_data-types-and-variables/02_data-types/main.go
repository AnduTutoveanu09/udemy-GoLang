package main

import "fmt"

func main(){
	//text - Go is Nice!
	var message string = "Go is Nice!"
	fmt.Println(message)

	//whole numbers - 14
	var number int = 14
	fmt.Println(number)
	
	//real numbers - 1.43
	var realNumber float32 = 1.43
	fmt.Println(realNumber)
	
	//characters = s, !
	var ch rune = 'S'
	fmt.Println(ch)
	
	//boolean - true or false
	var isTrue bool = true
	fmt.Println(isTrue)
}